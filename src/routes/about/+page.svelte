<h1 class="text-4xl text-center my-8 uppercase">The Current Club System Needs Work.</h1>
<h1 class="text-2xl text-center my-8">We want to fix it.</h1>
<p>
    The GCU Club System is buried in bureaucracy. 
</p>
    <p>Right now, you have to get 50 signatures from GCU students, a faculty advisor, a full-time staff and fill out a bunch of paperwork.</p>
    <p>For your great club, it might be worth it but it's hard to get started, and you can't focus on getting the word out about your club while working on starting it since unapproved clubs are barred from placing flyers around campus, among some other forms of advertising. Clubspace wants to fix that.</p>

    <p>
    This also affects the student experience by making it difficult for students to find people that share their interests through clubs since those clubs simply don't exist.
    </p>

<p>We give a platform where you can share your club and others can find it.</p>